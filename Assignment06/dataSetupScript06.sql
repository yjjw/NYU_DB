-- Create the database

CREATE TABLE Company(TIN VARCHAR(255) NOT NULL, RegNumber VARCHAR(255) NOT NULL,CONSTRAINT COMPANY_PK PRIMARY KEY (TIN));

CREATE TABLE Company_Domain(
				Domain_Name VARCHAR(255) NOT NULL,
				TIN VARCHAR(255) NOT NULL,
				CONSTRAINT COMPANY_DOMAIN_PK PRIMARY KEY (Domain_Name, TIN)
);

CREATE TABLE Big(TIN VARCHAR(255) NOT NULL, Company_Value INTEGER, Start_Date DATE, FamilyName VARCHAR(255), PersonalName VARCHAR(255), CONSTRAINT BIG_PK PRIMARY KEY (TIN) );

CREATE TABLE Small(
				TIN VARCHAR (255)NOT NULL,
				CONSTRAINT SMALL_PK PRIMARY KEY (TIN)
);

CREATE TABLE Helps(
				HELPER VARCHAR (255)NOT NULL,
				HELPED VARCHAR (255)NOT NULL,
				CONSTRAINT HELPS_PK PRIMARY KEY (HELPER, HELPED)
);

CREATE TABLE Manager(
				FamilyName VARCHAR (255)NOT NULL,
				PersonalName VARCHAR (255)NOT NULL,
				Salary INTEGER,
				CONSTRAINT MANAGER_PK PRIMARY KEY (FamilyName, PersonalName)
);



ALTER TABLE Company_Domain ADD CONSTRAINT COMPANY_DOMAIN_FK
